#!/usr/bin/env python3
import sys
import bisect

def solve(N: int, Q: int, A: "List[int]", X: "List[int]"):

    A.sort()
    A_cumsum = [0] * N
    A_cumsum[0] = A[0]
    for n in range(1, N):
        A_cumsum[n] = A_cumsum[n - 1] + A[n]

    for x in X:
        i = bisect.bisect_left(A, x)

        neg_num = i
        pos_num = N - i

        ans = 0
        if neg_num > 0:
            ans += (neg_num * x)
            ans -= A_cumsum[neg_num - 1]
        if pos_num > 0:
            ans -= (pos_num * x)
            ans += A_cumsum[N - 1]
            if neg_num > 0:
                ans -= A_cumsum[neg_num - 1]

        print(ans)








