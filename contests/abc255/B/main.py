import sys


def solve(N: int, K: int, A: "List[int]", X: "List[int]", Y: "List[int]"):
    
    dist_table = [float('Inf')]*N

    N_list = [False]*N
    for a in A:
        N_list[a - 1] = True
        dist_table[a - 1] = 0
    
    for a in A:
        a -= 1
        for n in range(N):
            if N_list[n]: continue

            ax = X[a]
            ay = Y[a]
            nx = X[n]
            ny = Y[n]
            dist = (ax - nx) ** 2 + (ay - ny) ** 2

            dist_table[n] = min(dist_table[n], dist)

    print(max(dist_table) ** 0.5)



