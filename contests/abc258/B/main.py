#!/usr/bin/env python3
import sys


def solve(N: int, A: "List[int]"):

    for i in range(N):
        A[i] = [int(x) for x in list(str(A[i]))]

    max_num = 0

    for i in range(N):
        for j in range(N):
            if max_num < A[i][j]:
                max_num = A[i][j]

    max_row_list = []
    max_col_list = []

    for i in range(N):
        for j in range(N):
            if max_num == A[i][j]:
                max_row_list.append(i)
                max_col_list.append(j)

    def get_number(r, c, dr, dc):
        result = 0
        units = [1000, 100, 10, 1]
        for i in range(N):
            nr = r + dr * i
            nc = c + dc * i
            nc %= N
            nr %= N
            unit = 10 ** (N - 1 - i)
            result += A[nr][nc] * unit

        return result

    ans = 0
    for r, c in zip(max_row_list, max_col_list):
        for dr in range(-1, 2):
            for dc in range(-1, 2):
                if dr == 0 and dc == 0:
                    continue
                result = get_number(r, c, dr, dc)
                if ans < result:
                    ans = result
    print(ans)


