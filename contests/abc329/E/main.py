#!/usr/bin/env python3
import sys
from collections import deque

YES = "Yes"  # type: str
NO = "No"  # type: str


def solve(N: int, M: int, S: str, T: str):

    S = list(S)

    def replace(i):
        nonlocal S
        for j in range(M):
            S[i + j] = "#"

    def is_equal(i):
        for j in range(M):
            if S[i + j] != "#" and S[i + j] != T[j]:
                return False
        replace(i)
        return True

    BUF_SIZE = N - M + 1

    used = [is_equal(i) for i in range(BUF_SIZE)]
    que = deque([i for i, flg in enumerate(used) if flg])

    while que:
        curr = que.popleft()

        head = max(0, curr - M + 1)
        tail = min(BUF_SIZE, curr + M)
        for next in range(head, tail):
            if not used[next] and is_equal(next):
                used[next] = True
                que.append(next)

    if S.count("#") == N:
        print(YES)
    else:
        print(NO)


