#!/usr/bin/env python3
import sys
from collections import deque


def create_cost_edges(n, us, vs, cs, directed=False, onebase=True):
    edges = [[] for _ in range(n)]
    for u, v, c in zip(us, vs, cs):
        if onebase: u, v = u - 1, v - 1     # convert zero-base index
        edges[u].append((v, c))
        if directed: edges[v].append((u, c))
    return edges


def solve(N: int, M: int, L: int, S: int, T: int, U: list[int], V: list[int], C: list[int]):
    graph = create_cost_edges(N, U, V, C)
    

    def solve(start_i, end_i):
        que = deque()
        que.append((start_i, 0, 0))    # (node, cnt_branch, total_cost)

        memo = set()
        
        while que:
            curr_i, curr_branch, curr_cost = que.popleft()
            if (curr_i == end_i) and (curr_branch == L) and (S <= curr_cost <= T):
                return True
            
            if (curr_branch >= L) or (curr_cost >= T):
                continue

            for next_i, next_cost in graph[curr_i]:
                next_item = (next_i, curr_branch + 1, curr_cost + next_cost)
                if next_item in memo:
                    continue
                que.append(next_item)
                memo.add(next_item)

        return False


    ans = []
    for i in range(N):
        if solve(0, i):
            ans.append(str(i + 1))
    
    print(" ".join(ans))



