import sys
from collections import defaultdict
from collections import deque

YES = "Yes"  # type: str
NO = "No"  # type: str
INF = float('inf')  # type: float


def solve(N: int, D: int, X: "List[int]", Y: "List[int]"):

    states = [NO] * N
    D2 = D ** 2

    edges = [[] for _ in range(N)]

    for i in range(N - 1):
        for j in range(i + 1, N):
            xi, yi = X[i], Y[i]
            xj, yj = X[j], Y[j]

            dist = (xi - xj) ** 2 + (yi - yj) ** 2
            if dist <= D2:
                edges[i].append(j)
                edges[j].append(i)

    que = deque()
    que.append(0)
    states[0] = YES

    while que:
        c = que.popleft()

        for n in edges[c]:
            if states[n] == YES:
                continue

            states[n] = YES
            que.append(n)

    for ans in states:
        print(ans)



