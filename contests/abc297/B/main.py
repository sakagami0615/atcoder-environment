def solve(S: str):
    b = []
    r = []
    k = None
    for i,s in enumerate(S):
        if s == "B":
            b.append(i)
        elif s == "R":
            r.append(i)
        elif s == "K":
            k = i

    b0, b1 = b
    r_min, r_max = min(r), max(r)
    if (b0 + b1) % 2 == 0:
        print(NO)
        return
    if not(r_min < k < r_max):
        print(NO)
        return

    print(YES)


