#!/usr/bin/env python3
import sys
import math

YES = "Yes"  # type: str
NO = "No"  # type: str

EPS = 2

def calc_ang(b, a, c):
    return math.acos((b ** 2 + c ** 2 - a ** 2) / (2 * b * c))

def calc_dist(a_x, a_y, b_x, b_y):
    return math.sqrt((a_x - b_x) ** 2 + (a_y - b_y) ** 2)


def solve(A_x: int, A_y: int, B_x: int, B_y: int, C_x: int, C_y: int, D_x: int, D_y: int):

    A_x += 100
    A_y += 100
    B_x += 100
    B_y += 100
    C_x += 100
    C_y += 100
    D_x += 100
    D_y += 100

    AB = calc_dist(A_x, A_y, B_x, B_y)
    BC = calc_dist(B_x, B_y, C_x, C_y)
    CD = calc_dist(C_x, C_y, D_x, D_y)
    DA = calc_dist(D_x, D_y, A_x, A_y)

    DB = calc_dist(D_x, D_y, B_x, B_y)
    AC = calc_dist(A_x, A_y, C_x, C_y)
    BD = calc_dist(B_x, B_y, D_x, D_y)
    CA = calc_dist(C_x, C_y, A_x, A_y)

    DAB = calc_ang(DA, DB, AB)
    ABC = calc_ang(AB, AC, BC)
    BCD = calc_ang(BC, BD, CD)
    CDA = calc_ang(CD, CA, DA)

    total = (DAB + ABC + BCD + CDA) * 180 / math.pi
    if total >= 360 - EPS:
        print(YES)
    else:
        print(NO)

    print(total)


