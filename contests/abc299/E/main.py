#!/usr/bin/env python3
import sys
from collections import deque


YES = "Yes"  # type: str
NO = "No"  # type: str

BLACK = 1
WHITE = 0


def solve(N: int, M: int, U: "List[int]", V: "List[int]", K: int, P: "List[int]", D: "List[int]"):

    edges = [[] for _ in range(N)]

    for u, v in zip(U, V):
        u -= 1
        v -= 1
        edges[u].append(v)
        edges[v].append(u)

    pd_list = []
    for p, d in zip(P, D):
        p -= 1
        pd_list.append((p, d))


    color_g = [BLACK] * N

    cond_sets = [set() for _ in range(K)]

    for i, (p, d) in enumerate(pd_list):
        que = deque()
        que.append((p, 0))

        is_visit = [False] * N

        while que:
            v, cnt = que.popleft()

            if cnt == d:
                cond_sets[i].add(v)
                continue

            color_g[v] = WHITE

            for n in edges[v]:
                if is_visit[n]: continue
                is_visit[n] = True
                que.append((n, cnt + 1))

    for cond_set in cond_sets:
        is_ok = False
        for v in cond_set:
            if color_g[v] == BLACK:
                is_ok = True
                break
        if not is_ok:
            print(NO)
            return

    s = ""
    for a in color_g:
        s += str(a)
    print(YES)
    print(s)



