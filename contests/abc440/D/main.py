#!/usr/bin/env python3
import sys
import bisect


# 参考: https://atcoder.jp/contests/abc440/submissions/72394387
def solve(N: int, Q: int, A: list[int], X: list[int], Y: list[int]):
    A.sort()

    B = [a - i for i, a in enumerate(A)]
    """
    print(A)
    print(B)
    """

    for x, y in zip(X, Y):
        l_idx = bisect.bisect_left(A, x)

        if (l_idx == N) or (A[l_idx] - x >= y):
            print(x + y - 1)
            continue

        r_idx = bisect.bisect_right(B, (x - l_idx) + y - 1)

        ans = x + y - 1 + (r_idx - l_idx)
        """
        print((l_idx, r_idx), ans)
        """
        print(ans)

    return


