INF = 2 ** 64 - 1


def solve(H: int, W: int, N: int, hs: list[int], ws: list[int]):
    
    h_data = [(i, h) for i, h in enumerate(hs)]
    w_data = [(i, w) for i, w in enumerate(ws)]

    h_data.sort(reverse=True, key=lambda x: x[1])
    w_data.sort(reverse=True, key=lambda x: x[1])
    
    ans = [[INF, INF] for _ in range(N)]

    curr_h, curr_w = 0, 0
    idx_h, idx_w = 0, 0
    used = set()

    for _ in range(N):        
        while h_data[idx_h][0] in used:
            idx_h += 1
        while w_data[idx_w][0] in used:
            idx_w += 1

        if H - curr_h == h_data[idx_h][1]:
            idx = h_data[idx_h][0]
            ans[idx] = [curr_h + 1, curr_w + 1]
            curr_w += ws[idx]
            used.add(idx)
        else:
            idx = w_data[idx_w][0]
            ans[idx] = [curr_h + 1, curr_w + 1]
            curr_h += hs[idx]
            used.add(idx)

    for a in ans:
        print(*a)



