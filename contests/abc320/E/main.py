#!/usr/bin/env python3
import sys
from heapq import heappop, heappush
from bisect import bisect_left



def solve(N: int, M: int, T: "List[int]", W: "List[int]", S: "List[int]"):

    que = []
    for i in range(N):
        heappush(que, i)

    ans = [0] * N

    stack_t = [[] for _ in range(M + 1)]

    for idx, (t, w, s) in enumerate(zip(T, W, S)):
        for stock in stack_t[idx]:
            heappush(que, stock)

        if not que:
            continue

        head = heappop(que)
        ans[head] += w

        m_idx = bisect_left(T, t + s)
        stack_t[m_idx].append(head)

    for a in ans:
        print(a)


