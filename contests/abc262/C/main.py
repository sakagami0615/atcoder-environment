from functools import reduce
def comb(n, r):
    r = min(r, n - r)
    numer = reduce(lambda x, y: x * y, range(n, n - r, -1), 1)
    denom = reduce(lambda x, y: x * y, range(1, r + 1), 1)
    return numer // denom


def solve(N: int, a: "List[int]"):

    ans = 0

    disable_set = set()
    corr_id_num = 0
    for i in range(N):
        if i + 1 == a[i]:
            corr_id_num += 1
        else:
            if i + 1 in disable_set:
                continue
            next_id = a[i]
            if a[next_id - 1] == i + 1:
                ans += 1
                disable_set.add(next_id)

    if corr_id_num > 1:
        ans += int(comb(corr_id_num, 2))

    print(ans)


