#!/usr/bin/env python3
import sys


def LCP(X, Y):

    i = j = 0
    while i < len(X) and j < len(Y):
        if X[i] != Y[j]:
            break

        i = i + 1
        j = j + 1

    return X[:i]


def solve(N: int, S: "List[str]"):

    s_list = [(i, s) for i, s in enumerate(S)]

    s_list.sort(key=lambda x:x[1])

    ans_list = []
    for i in range(N):
        n = -1
        if i - 1 >= 0:
            n = max(n, len(LCP(s_list[i][1], s_list[i - 1][1])))
        if i + 1 <= N - 1:
            n = max(n, len(LCP(s_list[i][1], s_list[i + 1][1])))

        ans_list.append((s_list[i][0], n))

    ans_list.sort(key=lambda x:x[0])

    for x in ans_list:
        ans = x[1]
        print(ans)


