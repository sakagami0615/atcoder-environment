#!/usr/bin/env python3
import sys


def solve(N: int, A: "List[str]"):

    for i in range(N):
        for j in range(i, N):
            if i == j: continue

            value = A[i][j]
            inv_value = A[j][i]

            if value == 'D':
                if inv_value != 'D':
                    print('incorrect')
                    return

            else:
                if value == 'W':
                    if inv_value != 'L':
                        print('incorrect')
                        return
                elif value == 'L':
                    if inv_value != 'W':
                        print('incorrect')
                        return

    print('correct')




