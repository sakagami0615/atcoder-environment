#!/usr/bin/env python3
import sys

MOD = 998244353  # type: int


def solve(S: str):

    N = 3001

    dp = [[0] * N for _ in range(len(S) + 1)]

    dp[0][0] = 1

    for i, s in enumerate(S):
        i += 1

        is_up = False
        is_down = False

        if s == "(":
            is_up = True
        elif s == ")":
            is_down = True
        else:
            is_up, is_down = True, True

        if is_up:
            for j in range(1, N):
                dp[i][j] += dp[i - 1][j - 1]
                dp[i][j] %= MOD
        if is_down:
            for j in range(N - 1):
                dp[i][j] += dp[i - 1][j + 1]
                dp[i][j] %= MOD

    print(dp[-1][0])




