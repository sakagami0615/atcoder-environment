#!/usr/bin/env python3
import sys

YES = "Yes"  # type: str
NO = "No"  # type: str



def solve(A):

    def check_row(col_id):
        appeared = set()
        for i in range(9):
            num = A[i][col_id]
            if num in appeared:
                return False
            appeared.add(num)
        return True

    def check_col(row_id):
        appeared = set()
        for i in range(9):
            num = A[row_id][i]
            if num in appeared:
                return False
            appeared.add(num)
        return True

    def check_chunk(chunk_id):
        chunk_row, chunk_col = chunk_id // 3, chunk_id % 3
        base_row, base_col = chunk_row * 3, chunk_col * 3
        appeared = set()
        for i in range(base_row, base_row + 3):
            for j in range(base_col, base_col + 3):
                num = A[i][j]
                if num in appeared:
                    return False
                appeared.add(num)
        return True


    for i in range(9):
        if not check_row(i) or not check_col(i) or not check_chunk(i):
            print(NO)
            return
    print(YES)


