#!/usr/bin/env python3
import sys


# エラストテレスのふるい
def SOE(N):
	
    prime_list = [i for i in range(N + 1)]
    prime_list[1] = 0
    
    n_search_max = int(N ** 0.5)
    
    for i in range(2, n_search_max + 1):
        if not prime_list[i]: continue
        
        for j in range(i**2, N + 1, i):
        	prime_list[j] = 0
    
    prime_list = list(set(prime_list))
    return prime_list[1:]


def solve(N: int):

    n_size = int(N ** (1/3))

    prime_list = SOE(n_size)
    n_prime = len(prime_list)

    ans = 0
    for i in range(n_prime - 1):
        for j in range(i + 1, n_prime):
            p = prime_list[i]
            q = prime_list[j]
            if p * (q ** 3) <= N:
                ans += 1
            else:
                break

    print(ans)


