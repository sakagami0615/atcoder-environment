import sys


def solve(N: int, K: int, A: "List[int]"):

    num = {i + 1: 2 for i in range(N)}
    for a in A:
        num[a] -= 1

    ans = 0

    prev = 0
    for i in range(N):
        a = i + 1
        print([n for n in num.values()])
        if num[a] == 0:
            continue
        if prev > 0:
            ans += a - prev
            num[prev] -= 1
            num[a] -= 1
            if num[a] == 0:
                prev = 0
                continue

        if num[a] % 2 == 1:
            prev = a
            num[a] = 1
        else:
            prev = 0
            num[a] = 0

    print([n for n in num.values()])
    print(ans)


