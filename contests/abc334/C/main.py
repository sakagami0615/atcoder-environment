#!/usr/bin/env python3
import sys


def solve(N: int, K: int, A: "List[int]"):

    is_even = (2 * N - K) % 2 == 0

    head_label = 0
    if A[0] != 1:
        A = [1] + A
        head_label = 1
    if A[-1] != N:
        A.append(N)

    diff_a = []
    for i in range(len(A) - 1):
        diff_a.append(A[i + 1] - A[i])

    cumsum = [[0, 0] for _ in range(len(diff_a) + 1)]

    label = head_label
    for i, d in enumerate(diff_a):
        cumsum[i + 1][label] = cumsum[i][label] + d
        cumsum[i + 1][1 - label] = cumsum[i][1 - label]
        label = 1 - label

    if is_even:
        print(cumsum[-1][0])
        return

    ans = float("INF")
    for i in range(len(diff_a)):
        curr = cumsum[i][0] + (cumsum[-1][1] - cumsum[i + 1][1])
        ans = min(ans, curr)
    print(ans)


