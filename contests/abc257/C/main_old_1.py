def main():
    def iterate_tokens():
        for line in sys.stdin:
            for word in line.split():
                yield word
    tokens = iterate_tokens()
    N = int(next(tokens))  # type: int
    S = next(tokens)  # type: str
    W = [int(next(tokens)) for _ in range(N)]  # type: "List[int]"
    solve(N, S, W)

if __name__ == '__main__':
    main()
