def solve(S: str):
    
    n_a = 0
    n_b = 0

    ans = 0
    for s in S:
        if s == "A":
            n_a += 1
        elif s == "B":
            if n_a > 0:
                n_b += 1
                n_b = min(n_b, n_a)
        else:
            if n_a > 0 and n_b > 0:
                ans += 1
                n_a -= 1
                n_b -= 1
        
    print(ans)



