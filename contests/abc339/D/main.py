#!/usr/bin/env python3
import sys
from collections import deque


def solve(N: int, S: "List[str]"):

    def out_of_range(i, j):
        return i < 0 or j < 0 or i >= N or j >= N

    p1_pos, p2_pos = None, None
    for i in range(N):
        for j in range(N):
            if S[i][j] == "P":
                if p1_pos is None:
                    p1_pos = (i, j)
                else:
                    p2_pos = (i, j)

    que = deque()
    que.append((*p1_pos, *p2_pos, 0))

    used = [[[[False] * N for _ in range(N)] for _ in range(N)] for _ in range(N)]

    while que:
        p1_i, p1_j, p2_i, p2_j, cnt = que.popleft()

        if p1_i == p2_i and p1_j == p2_j:
            print(cnt)
            return

        for di, dj in [(1, 0), (-1, 0), (0, 1), (0, -1)]:
            p1_ni, p1_nj = p1_i + di, p1_j + dj
            p2_ni, p2_nj = p2_i + di, p2_j + dj
            if out_of_range(p1_ni, p1_nj) or S[p1_ni][p1_nj] == "#":
                p1_ni, p1_nj = p1_i, p1_j
            if out_of_range(p2_ni, p2_nj) or S[p2_ni][p2_nj] == "#":
                p2_ni, p2_nj = p2_i, p2_j

            if not used[p1_ni][p1_nj][p2_ni][p2_nj]:
                used[p1_ni][p1_nj][p2_ni][p2_nj] = True
                que.append((p1_ni, p1_nj, p2_ni, p2_nj, cnt + 1))

    print(-1)

