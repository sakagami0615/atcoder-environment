YES = "Yes"  # type: str
NO = "No"  # type: str


def solve(N: int, P: int, Q: int, R: int, A: "List[int]"):

    A_cumsum = [0] * (N + 1)
    for i, a in enumerate(A):
        A_cumsum[i + 1] = A_cumsum[i] + a

    for x in range(0, N):
        y = bisect_right(A_cumsum, P + A_cumsum[x])
        if A_cumsum[y - 1] != P + A_cumsum[x]:
            continue

        z = bisect_right(A_cumsum, Q + A_cumsum[y - 1])
        if A_cumsum[z - 1] != Q + A_cumsum[y - 1]:
            continue

        w = bisect_right(A_cumsum, R + A_cumsum[z - 1])
        if A_cumsum[w - 1] != R + A_cumsum[z - 1]:
            continue

        print(YES)
        return

    print(NO)




