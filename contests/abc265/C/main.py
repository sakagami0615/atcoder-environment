def main():
    H, W = map(int, input().split())

    G = [[''] * (W + 2) for _ in range(H + 2)]

    for i in range(H):
        tmp = input()
        for j in range(W):
            G[i + 1][j + 1] = tmp[j]

    solve(H, W, G)


if __name__ == '__main__':
    main()
