def solve(N: int, A: list[int]):
    
    a_max = max(A)
    bit = fenwicktree.FenwickTree(a_max + 1)

    for a in A:
        bit.add(a, 1)

    n_ans = a_max + 1
    ans = [0] * n_ans
    for i in range(a_max):
        v = N - bit.sum(0, i + 1)
        p0 = ans[n_ans - 1 - i]
        p1 = ans[n_ans - 1 - i - 1]
        ans[n_ans - 1 - i] = (p0 + v) % 10
        ans[n_ans - 1 - i - 1] = p1 + (p0 + v) // 10
    
    if ans[0] != 0:
        print(ans[0], end="")
    for a in ans[1:]:
        print(a, end="")
    print()


