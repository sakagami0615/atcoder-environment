#!/usr/bin/env python3
import sys
from collections import defaultdict, Counter

YES = "Yes"  # type: str
NO = "No"  # type: str
INF = float('inf')  # type: float

TEMP_SET = set("atcoder")


def solve(S: str, T: str):
    s_cnt = Counter(S)
    t_cnt = Counter(T)

    col = set()
    for k in s_cnt.keys():
        col.add(k)
    for k in t_cnt.keys():
        col.add(k)

    s_diff, t_diff = 0, 0
    for c in col:
        if c == "@": continue
        s_n = 0 if c not in s_cnt else s_cnt[c]
        t_n = 0 if c not in t_cnt else t_cnt[c]

        if s_n != t_n and c not in TEMP_SET:
            print(NO)
            return

        s_diff += max(t_n - s_n, 0)
        t_diff += max(s_n - t_n, 0)

    if s_cnt["@"] >= s_diff and t_cnt["@"] >= t_diff:
        print(YES)
    else:
        print(NO)




