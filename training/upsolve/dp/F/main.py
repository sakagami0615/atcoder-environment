def solve(s: str, t: str):

    ns, nt = len(s), len(t)

    dp = [[0] * (nt + 1) for _ in range(ns + 1)]

    for i in range(ns):
        for j in range(nt):
            dp[i + 1][j + 1] = max(dp[i + 1][j + 1], dp[i][j + 1])
            dp[i + 1][j + 1] = max(dp[i + 1][j + 1], dp[i + 1][j])

            if s[i] == t[j]:
                dp[i + 1][j + 1] = max(dp[i + 1][j + 1], dp[i][j] + 1)

    rans = ""
    i, j = ns, nt

    while (i != 0 or j != 0) and (dp[i][j] != 0):

        if dp[i][j] == dp[i - 1][j]:
            i -= 1
        elif dp[i][j] == dp[i][j - 1]:
            j -= 1
        else:
            rans += s[i - 1]
            i -= 1
            j -= 1

        i, j = max(0, i), max(0, j)

    print(rans[::-1])


