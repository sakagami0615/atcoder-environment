import sys
sys.setrecursionlimit(10**8)



def solve(N: int, M: int, X: "List[int]", Y: "List[int]"):

    edges = [[] for _ in range(N)]
    for x, y in zip(X, Y):
        x -= 1
        y -= 1
        edges[y].append(x)

    memo = [-1] * N

    def dp(v):
        nonlocal memo
        if memo[v] != -1:
            return memo[v]

        if len(edges[v]) == 0:
            memo[v] = 0
            return 0

        value = 0
        for n in edges[v]:
            value = max(value, dp(n))

        memo[v] = value + 1
        return memo[v]

    for i in range(N):
        dp(i)

    print(max(memo))



