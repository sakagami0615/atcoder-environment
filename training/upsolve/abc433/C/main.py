def solve(S: str):
    
    datas = [[int(S[0]), 1]]
    for c in S[1:]:
        c = int(c)
        if datas[-1][0] == c:
            datas[-1][1] += 1
        else:
            datas.append([c, 1])

    ans = 0
    for i in range(len(datas) - 1):
        if datas[i][0] + 1 == datas[i + 1][0]:
            ans += min(datas[i][1], datas[i + 1][1])
    print(ans)


