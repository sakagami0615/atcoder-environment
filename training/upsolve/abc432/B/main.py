def solve(X: int):

    data = [int(d) for d in str(X)]

    zero_cnt = 0
    nonzero_data = []
    for d in data:
        if d == 0: zero_cnt += 1
        else: nonzero_data.append(d)

    nonzero_data.sort()
    nonzero_data.append("#")

    ans = ""
    for i, d in enumerate(nonzero_data):
        if i == 1:
            ans += "0" * zero_cnt
        if d == "#":
            break
        ans += str(d)
    print(ans)


