def create_simple_edges(n: int, us: list[int], vs: list[int], directed: bool = True, onebase: bool = True) -> list[list[int]]:
    # directed: 有向グラフかどうか, onebase: 1-indexかどうか
    edges = [[] for _ in range(n)]
    for u, v in zip(us, vs):
        if onebase: u, v = u - 1, v - 1     # convert zero-base index
        edges[u].append(v)
        if directed: edges[v].append(u)
    return edges


