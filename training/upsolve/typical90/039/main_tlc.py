import sys
from heapq import heappop, heappush

def dijkstra(edges, n_node, start):
    dist = [float('Inf')] * n_node
    used = [False] * n_node

    route = [float('Inf')] * n_node

    dist[start] = 0
    used[start] = True
    queue = [(dist[start], start)]

    while queue:
        _, now = heappop(queue)
        used[now] = True
        for next, weight in edges[now]:
            if (not used[next]) and (dist[now] + weight < dist[next]):
                dist[next] = dist[now] + weight
                route[next] = now
                heappush(queue, (dist[next], next))
    return dist, route

def solve(N: int, A: "List[int]", B: "List[int]"):

    edges = [[] for _ in range(N)]

    for a, b in zip(A, B):
        a, b = a - 1, b - 1
        edges[a].append((b, 1))
        edges[b].append((a, 1))

    ans = 0
    for i in range(N - 1):
        dists, _ = dijkstra(edges, N, i)
        for j in range(i + 1, N):
            ans += dists[j]
    print(ans)


