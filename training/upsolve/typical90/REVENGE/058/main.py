import sys
from collections import defaultdict

MOD = 10 ** 5  # type: int

def calc(n):
    ret = n
    while n > 0:
        ret += n % 10
        n //= 10
    ret %= MOD
    return ret

def solve(N: int, K: int):

    calc_map = {}
    for i in range(MOD):
        calc_map[i] = calc(i)

    cnt_map = {}
    curr_n = N
    cnt = 0
    while curr_n not in cnt_map:
        cnt_map[curr_n] = cnt
        curr_n = calc_map[curr_n]
        cnt += 1

    cycle = cnt - cnt_map[curr_n]
    if K >= cnt_map[curr_n]:
        K = (K - cnt_map[curr_n]) % cycle + cnt_map[curr_n]

    for k, v in cnt_map.items():
        if v == K:
            print(k)



