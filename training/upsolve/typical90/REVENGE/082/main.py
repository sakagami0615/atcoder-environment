import sys
from math import log10

MOD = 1000000007  # type: int

# Get the value of a/b (mod)
def mod_div(a, b, mod=MOD):
    return (a * pow(b, mod - 2, mod)) % mod


# sum = n/2 * (n + 1)
def seq_sum(num, mod=MOD):
    n1 = num % mod
    n2 = (num + 1) % mod
    v = (n1 * n2) % mod
    return mod_div(v, 2)


def solve(L: int, R: int):

    ans = 0
    for i in range(1, 20):
        vl = max(L, 10 ** (i - 1))
        vr = min(R, 10 ** i - 1)
        if vl > vr:
            continue

        val = (seq_sum(vr) - seq_sum(vl - 1) + MOD) % MOD
        ans += i * val
        ans %= MOD

    print(ans)




