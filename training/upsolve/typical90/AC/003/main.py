import sys



from collections import deque

def max_minroute(G, start):

    deq = deque()
    deq.append((0, start))

    visit_set = set()
    visit_set.add(start)

    max_dist = 0
    max_city = start
    while deq:
        dist, curr = deq.popleft()

        if max_dist < dist:
            max_dist = dist
            max_city = curr

        for next in G[curr]:
            if next not in visit_set:
                visit_set.add(next)
                deq.append((dist + 1, next))

    return max_dist, max_city
    


def solve(N: int, A: "List[int]", B: "List[int]"):
    
    G = [[] for _ in range(N)]

    for a, b in zip(A, B):
        a -= 1
        b -= 1
        G[a].append(b)
        G[b].append(a)
    
    _, city = max_minroute(G, 0)
    dist, city = max_minroute(G, city)

    print(dist + 1)

    


