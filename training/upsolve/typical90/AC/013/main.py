#!/usr/bin/env python3
import sys


from heapq import heappop, heappush

def dijkstra(edges, n_node, start):
    dist = [float('Inf')] * n_node
    used = [False] * n_node

    dist[start] = 0
    used[start] = True
    queue = [(dist[start], start)]

    while queue:
        _, now = heappop(queue)
        used[now] = True
        for next, weight in edges[now]:
            if (not used[next]) and (dist[now] + weight < dist[next]):
                dist[next] = dist[now] + weight
                heappush(queue, (dist[next], next))
    return dist


def solve(N: int, M: int, A: "List[int]", B: "List[int]", C: "List[int]"):

    edges = [[] for _ in range(N)] 
    for a, b, c in zip(A, B, C):
        a -= 1
        b -= 1
        edges[a].append((b, c))
        edges[b].append((a, c))
    
    dist_1 = dijkstra(edges, N, 0)
    dist_2 = dijkstra(edges, N, N - 1)

    for i in range(N):
        print(dist_1[i] + dist_2[i])



