    def norm_pos(pos):
        min_x = min(p[0] for p in pos)
        min_y = min(p[1] for p in pos)
        return set((p[0] - min_x, p[1] - min_y) for p in pos)

    def shift_pos(pos, shift):
        return set((p[0] - shift[0], p[1] - shift[1]) for p in pos)

