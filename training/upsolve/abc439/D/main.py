INF = 2 ** 64 - 1


def solve(N: int, A: list[int]):
    
    idx_map = defaultdict(list)
    for i, a in enumerate(A):
        idx_map[a].append(i)
    
    ans = 0
    for j in range(N):
        if A[j] % 5 == 0:
            d = A[j] // 5
            ai = 7 * d
            ak = 3 * d

            ai_idx = idx_map[ai]
            ak_idx = idx_map[ak]

            # j max
            if j > 0:
                n_ai = bisect_left(ai_idx, j)
                n_ak = bisect_left(ak_idx, j)
                ans += n_ai * n_ak
            
            # j min
            if j < N - 1:
                n_ai = len(ai_idx) - bisect_right(ai_idx, j)
                n_ak = len(ak_idx) - bisect_right(ak_idx, j)
                ans += n_ai * n_ak

    print(ans)


