def solve(N: int, A: list[int]):
    
    cumsum = [defaultdict(int) for _ in range(N + 1)]

    for i, a in enumerate(A):
        for k in cumsum[i].keys():
            if cumsum[i][k] > 0:
                cumsum[i + 1][k] = cumsum[i][k]
        cumsum[i + 1][a] += 1
    
    ans = 0
    for j in range(N):
        if A[j] % 5 == 0:
            d = A[j] // 5
            ai = 7 * d
            ak = 3 * d

            # j max
            if j > 0:
                n_ai = cumsum[j][ai]
                n_ak = cumsum[j][ak]
                ans += n_ai * n_ak
            
            # j min
            if j < N - 1:
                n_ai = cumsum[N][ai] - cumsum[j][ai]
                n_ak = cumsum[N][ak] - cumsum[j][ak]
                ans += n_ai * n_ak

    print(ans)


